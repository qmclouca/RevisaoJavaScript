//Math.abs(): Retorna o valor absoluto de um número.
console.log(Math.abs(-42));   // 42
console.log(Math.abs(42));    // 42

//Math.ceil(): Retorna o menor inteiro maior ou igual ao número fornecido.
console.log(Math.ceil(42.1));  // 43

//Math.floor(): Retorna o maior inteiro menor ou igual ao número fornecido.
console.log(Math.floor(42.9));  // 42

//Math.round(): Arredonda o número para o inteiro mais próximo.
console.log(Math.round(42.4));  // 42
console.log(Math.round(42.5));  // 43

//Math.max(): Retorna o maior dos números fornecidos.
console.log(Math.max(3, 42, 5, 23));  // 42

//Math.min(): Retorna o menor dos números fornecidos.
console.log(Math.min(3, 42, 5, 23));  // 3

//Math.random(): Retorna um número flutuante pseudoaleatório entre 0 (inclusive) e 1 (exclusivo).
console.log(Math.random());  // um número entre 0 e 1, como 0.1234567894321

//Math.pow(): Retorna a base elevada à potência do expoente.
console.log(Math.pow(2, 3));  // 8

//Math.sqrt(): Retorna a raiz quadrada de um número.
console.log(Math.sqrt(49));   // 7

//Math.sin(), Math.cos(), Math.tan(): Retorna o seno, cosseno e tangente de um número, respectivamente. O valor do número é em radianos.
console.log(Math.sin(Math.PI / 2));  // 1 (seno de 90 graus)

//Math.log(): Retorna o logaritmo natural (base e) de um número.
console.log(Math.log(Math.E));  // 1, pois o logaritmo natural de e é 1

//Math.exp(): Retorna e^x, onde x é o argumento e e é a constante de Euler.
console.log(Math.exp(1));  // 2.718281828459045, que é o valor de e